frase = 'Curso em Vídeo Python'

print(frase.replace("python", "Android"))  # mudara a palavra python por android
print(frase.upper())  # deixa todas as letras em maiuscula
print(frase.lower())  # deixa todas as letras em minuscula
print(frase.capitalize())  # deixa todas as letras em minuscula, e a primeira letra em maiuscula
print(frase.title())  # deixa todas as primeira palavras em maiuscula
print(frase.strip())  # remover os espaços do inicio e fim, deixando so os espaços do meio da frase
print(frase.rstrip())  # remover os espaços do fim, deixando so os espaços do começo e do meio da frase
print(frase.lstrip())  # remover os espaços do começo, deixando so os espaços do fim e do meio da frase
