

n = input("Digite um valor: ")
print(n.isnumeric())  # é númerico?
print(n.isalpha())  # é letra?
print(n.isalnum())  # é letra e número?
print(n.isupper())  # é letra maiúsculas?
print(n.islower())  # é letra minúsculas?
print(n.isspace())  # tem espaços?
print(n.istitle())  # inicia com letra maiúscula?
