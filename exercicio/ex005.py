# Faça um programa que leia um número inteiro
# e mostrar na tela o seu sucessor e seu antecessor.

n = int(input('Digite um número: '))  # Se digitar o número 2 mostrara 1 antecessor e 3 o sucessor.

print(f'Analisando o valor {n}, seu antecessor {n-1} e o seu sucessor é {n+1}.')

# Resultado do print
# Analisando o valor 2 seu antecessor é 1 e o seu sucessor é 3.
